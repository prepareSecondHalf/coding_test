# n: 가로길이 
# 출력: 2 * n 크기의 바닥을 채우는 방법의 수를 796796으로 나눈 나머지
# 문제: 1*2, 2*1, 2*2 3가지 덮개를 이용해 바닥을 채우는 경우의수 % 796796
# n = 1 -> 1, n = 2 -> 3, n = 3 -> 5
# 경우의수 = n-1일때의 경우의수 + 2 * (n-2일때 경우의수)
# n-1일때 경우의수일때 세로길이2짜리만 채울수 있고 n-2 경우의수에 가로2짜리두개 또는 2*2짜리 두가지만 채울수있음
import sys

n = int(sys.stdin.readline())
d = [0] * 1001

d[1] = 1
d[2] = 3

for i in range(3, n+1):
    d[i] = d[i-1] + 2 * d[i-2]
print(d[n])